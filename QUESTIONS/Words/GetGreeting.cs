namespace Practice_Problems.Questions.Words;

public class GetGreeting
{
    public string GetGreeting101(string nameOfPerson)
    {
        if (nameOfPerson == "")
        {
            return "Hello!";
        }
        else
        {
            return $"Hello, {nameOfPerson}!";
        }
    }
}

